//OPERADORES DE COMPARACION

/*
    ==
    Compara si dos valores son iguales
*/

console.log(1 == 1); //true

//Si queremos comparar que "1" (string) es 1 (numero)
console.log("1" == 1); //devuelve true porque compara el VALOR y no el TIPO

/*
    ===
    Compara si el valor y tipo son ESTRICTAMENTE iguales
*/

console.log("1" === 1) //devuelve false porque no el TIPO no es igual

/*
    !=
    Compara si dos valores son diferentes
*/

console.log(1 != 1); //false
/*
    !==
    Compara si el valor y tipo son ESTRICTAMENTE diferentes
*/

console.log("1" !== 1) //devuelve true porque no el TIPO no es igual

/* 
    > 
    mayor que
*/
console.log(5 > 4)
console.log(5 < 4)

/* 
    <
    menorque
*/
console.log(5 < 4)
console.log(5 > 4)

/* 
    >= 
    mayor o igual
    
    <= 
    menor o igual
*/
console.log (5 <= 4)
console.log (5 >= 5)
























